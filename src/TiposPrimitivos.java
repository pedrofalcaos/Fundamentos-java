public class TiposPrimitivos {

    public static void main(String[] args) {
        short a = 10;
        System.out.println(a);

        a = 20;
        System.out.println(a);

        short notaDePedro = 10;
        System.out.println(notaDePedro);

        double escola = 3.1;
        System.out.println(escola);


       // Conversão da tabela ASCI, ele irá imprimir a letra correspondente ao número
        char letra = 75;
        System.out.println(letra);
        // As aspas tem poder de transformar o inteiro em caracter, um inteiro pode virar uma STRING
        System.out.println("48"+5);
        // Sempre que tiver uma impressão irá ser feita da esquerda para direita
        System.out.println("" + 48 +5);
        System.out.println(48 + 5 + "");








    }


}
